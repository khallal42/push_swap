#include "checker.h"

int	ft_strcmp(char *tmp, char *mv)
{
	int	i;

	i = 0;
	while (tmp[i] || mv[i])
	{
		if (tmp[i] != mv[i])
			return (0);
		i++;
	}
	return (1);
}
